const employees = [
    { id: 1, name: 'John Doe', age: 30, department: 'IT', salary: 50000 },
    { id: 2, name: 'Alice Smith', age: 28, department: 'HR', salary: 45000 },
    { id: 3, name: 'Bob Johnson', age: 35, department: 'Finance', salary: 60000 },
    //... More employee records can be added here
  ];

function clear_scr(){
    document.getElementById("employeesDetails").innerHTML = "";
}

function displayEmployees(){
  const totalEmployees = employees.map((emp, index) => 
      `<p>
          Employee ID : ${emp.id}<br>
          Employee Name : ${emp.name}<br>
          Age : ${emp.age}<br>
          Department : ${emp.department}<br>
          Salary : ${emp.salary}
      </p>`
  ).join();

  document.getElementById("employeesDetails").innerHTML = totalEmployees;
}

function calculateTotalSalaries(){
    const totalSalaries = employees.reduce((acc, emp) => acc+emp.salary,0);
    alert(`Total Salaries: ${totalSalaries}`);
}

function displayHREmployees(){
    const hrEmployees = employees.filter((emp) => emp.department === "HR");
    const hrEmployeesDisplay = hrEmployees.map((emp, index) => 
        `<p>
            Employee ID : ${emp.id}<br>
            Employee Name : ${emp.name}<br>
            Age : ${emp.age}<br>
            Department : ${emp.department}<br>
            Salary : ${emp.salary}
        </p>`
    ).join();
  
    document.getElementById("employeesDetails").innerHTML = hrEmployeesDisplay;
  }

function findEmployeeById(employeeId) {
    const foundEmployee = employees.find(employee => employee.id === employeeId);
    console.log("Found Object: ",foundEmployee);
    if (foundEmployee) {
        document.getElementById('employeesDetails').innerHTML =`<p>${foundEmployee.id}: ${foundEmployee.name}: ${foundEmployee.name} - ${foundEmployee.department} - $${foundEmployee.salary}</p>`;
    }
    else{
      document.getElementById('employeesDetails').innerHTML = 'no employee has been found with this ID';

    }
}